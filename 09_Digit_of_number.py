# 09 Показать первую и последнюю цифру трёхзначного числа

def digit(x):
    x = x % 10
    return x

def dig(x):
    x = (x - x % 100)//100
    return x


numb = int(input('Введите трехзначное число: '))
if (abs(numb) < 99 or abs(numb) > 999): # по модулю на случай отрицательного ввода
    print ('Вы ввели не трехзначное число, попробуйте снова.')
    exit() 
else:
    print ('Последняя цифра числа:', digit(numb))
    print ('Первая цифра числа:', dig(abs(numb))) # по модулю на случай отрицательного ввода


